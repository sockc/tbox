#!/usr/bin/env bash

custom_menu() {
    local choice

    while true; do
        print_header "自建脚本集合"
        cat <<'EOF'
 1. 服务器初始化脚本（占位）
 2. 常用环境安装脚本（占位）
 3. 自建面板/服务脚本（占位）
 4. 代理相关自建脚本（占位）
 5. Docker 应用脚本（占位）
 6. 网络/防火墙辅助脚本（占位）
 7. 维护/清理脚本（占位）
 8. 查看规划说明
 0. 返回上一级
EOF
        echo
        read -r -p "请输入选项: " choice

        case "$choice" in
            1)
                info "后续这里放你的服务器初始化类脚本。"
                ;;
            2)
                info "后续这里放你的常用环境安装类脚本。"
                ;;
            3)
                info "后续这里放你自己构建的面板/服务类脚本。"
                ;;
            4)
                info "后续这里放代理相关的自建脚本。"
                ;;
            5)
                info "后续这里放 Docker 应用部署类脚本。"
                ;;
            6)
                info "后续这里放网络/防火墙辅助类脚本。"
                ;;
            7)
                info "后续这里放维护/清理/巡检类脚本。"
                ;;
            8)
                cat <<'EOT'
这个菜单用于集中管理你自己构建的脚本集合。

建议后续按类别拆分：
- 服务器初始化
- 常用软件安装
- 面板/服务部署
- 代理相关
- Docker 应用
- 网络防火墙
- 维护清理

这样后续扩展时不会把主菜单搞乱。
EOT
                ;;
            0)
                return
                ;;
            *)
                warn "无效选项"
                ;;
        esac

        pause
    done
}
